// Menggunakan operator matematika sebagai proses untuk melakukan perhitungan
const a = 10;
const b = 2;

// Penjumlahan
console.log('Penjumlahan (a+b) =', a + b);

// Pengurangan
console.log('Pengurangan (a-b) =', a - b);

// Perkalian
console.log('Perkalian (a*b) =', a * b);

// Pembagian
console.log('Pembagian (a/b) =', a / b);

// Modulus, hasil sisa
console.log('Modulus (a%b) =', a % b);