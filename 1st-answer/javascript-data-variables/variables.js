// Penggunaan var (jenis variabel yang digunakan berulang kali dan saat ini sangat jarang digunakan!)
var warnaMobil = "merah";
warnaMobil = "hitam";
console.log(warnaMobil);

// Penggunaan const (jenis variabel yang tidak akan digunakan berulang kali)
const warnaMeja = "biru";
console.log(warnaMeja);
const phi = 3.14;
console.log(phi);

// Penggunaan let (jenis variabel yang dapat digunakan berulang kali seperti var)
let warnaMotor = "kuning";
warnaMotor = "coklat"
console.log(warnaMotor); //coklat

// Pastikan untuk menggunakan CONST atau LET !!!
